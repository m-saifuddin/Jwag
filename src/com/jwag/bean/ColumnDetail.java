package com.jwag.bean;

import java.io.Serializable;

/**
 *
 * @author Saifo
 */
public class ColumnDetail implements Serializable {
    
    private String tableName;
    private String columnName;
    private String columnJavaName;
    private String columnJavaType;
    private String jstlGetExpr;
    private int columnSize;
    private boolean columnNull;
    private String columnTypeName;

    public String getTableName() {
        return tableName;
    }

    /**
     * 
     * @param tableName 
     */
    public void setTableName(String tableName) {
        this.tableName = tableName;
    }

    public String getColumnName() {
        return columnName;
    }

    /**
     * 
     * @param columnName 
     */
    public void setColumnName(String columnName) {
        this.columnName = columnName;
    }

    /**
     * 
     * @return 
     */
    public String getColumnJavaName() {
        return columnJavaName;
    }

    /**
     * 
     * @param columnJavaName 
     */
    public void setColumnJavaName(String columnJavaName) {
        this.columnJavaName = columnJavaName;
    }

    /**
     * 
     * @return 
     */
    public String getColumnJavaType() {
        return columnJavaType;
    }

    /**
     * 
     * @param columnJavaType 
     */
    public void setColumnJavaType(String columnJavaType) {
        this.columnJavaType = columnJavaType;
    }

    /**
     * 
     * @return 
     */
    public String getJstlGetExpr() {
        return jstlGetExpr;
    }

    /**
     * 
     * @param jstlGetExpr 
     */
    public void setJstlGetExpr(String jstlGetExpr) {
        this.jstlGetExpr = jstlGetExpr;
    }

    /**
     * 
     * @return 
     */
    public int getColumnSize() {
        return columnSize;
    }

    /**
     * 
     * @param columnSize 
     */
    public void setColumnSize(int columnSize) {
        this.columnSize = columnSize;
    }

    /**
     * 
     * @return 
     */
    public boolean isColumnNull() {
        return columnNull;
    }

    /**
     * 
     * @param columnNull 
     */
    public void setColumnNull(boolean columnNull) {
        this.columnNull = columnNull;
    }

    /**
     * 
     * @return 
     */
    public String getColumnTypeName() {
        return columnTypeName;
    }

    /**
     * 
     * @param columnTypeName 
     */
    public void setColumnTypeName(String columnTypeName) {
        this.columnTypeName = columnTypeName;
    }
    

}